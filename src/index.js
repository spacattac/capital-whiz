var APP_ID = undefined;

var SKILL_NAME = "Capital Whiz";
var GET_FACT_MESSAGE = "The state capital of ";
var WELCOME_MESSAGE = "Welcome to Capital Whiz";
var HELP_MESSAGE = "As an example, you can say what is the capital of Hawaii.";
var HELP_REPROMPT = "What can I help you with?";
var STOP_MESSAGE = "Goodbye!";

//=========================================================================================================================================
//TODO: Replace this data with your own.  You can find translations of this data at http://github.com/alexa/skill-sample-node-js-fact/data
//=========================================================================================================================================
var data = [
    { 
        "state": "Washington",
        "capital": "Olympia"
    }
];

//=========================================================================================================================================
//Editing anything below this line might break your skill.  
//=========================================================================================================================================
exports.handler = function(event, context, callback) {
    var alexa = Alexa.handler(event, context);
    alexa.APP_ID = APP_ID;
    alexa.registerHandlers(handlers);
    alexa.execute();
};

var handlers = {
    'LaunchRequest': function () {
        this.emit(':tell', WELCOME_MESSAGE);
    },
    'ReciteCapitalsIntent': function () {
        var factArr = data;
        var factIndex = 0;
        var randomFact = factArr[factIndex];
        var speechOutput = GET_FACT_MESSAGE + randomFact.state + ' is ' + randomFact.capital;
        this.emit(':tellWithCard', speechOutput, SKILL_NAME, randomFact)
    },
    'AMAZON.HelpIntent': function () {
        var speechOutput = HELP_MESSAGE;
        var reprompt = HELP_REPROMPT;
        this.emit(':ask', speechOutput, reprompt);
    },
    'AMAZON.CancelIntent': function () {
        this.emit(':tell', STOP_MESSAGE);
    },
    'AMAZON.StopIntent': function () {
        this.emit(':tell', STOP_MESSAGE);
    }
};